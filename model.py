class RMSNorm(nn.Module):
    def __init__(self, dim, elementwise_affine=False, eps=1e-6):
        super().__init__()
        self.scale = dim ** 0.5
        self.g = nn.Parameter(torch.ones(1, 1, dim))

    def forward(self, x):
        return F.normalize(x, dim=2) * self.g * self.scale


class BatchNorm(nn.Module):
    def __init__(self, dim, elementwise_affine=False, eps=1e-6):
        super().__init__()
        # self.scale = dim ** 0.5
        # self.g = nn.Parameter(torch.ones(1, 1, dim))

        self.norm = nn.BatchNorm1d(dim, affine=False)

    def forward(self, x):
        x = x.permute(0, 2, 1)
        x = self.norm(x)
        return x.permute(0, 2, 1)


